// 답안(각 문항의 선택 점수 0~100, 미응답은 null) → 가중평균 0~100
export function computeScore(answers) {
  let wsum = 0, w = 0;
  QUIZ.forEach((item, i) => {
    const a = answers[i];
    if (a != null) { wsum += item.weight * a; w += item.weight; }
  });
  return w ? Math.round(wsum / w) : 0;
}

